
public class Wall {
	private int x,y,width,height;
	
	public Wall(int x,int y,int width,int height)
	{
		this.x = x;
		this.y = y;
		this.width = width;
		this.height = height;
	}
	
	public int X()
	{
		return x;
	}
	
	public int Y()
	{
		return y;
	}
	
	public int Width()
	{
		return width;
	}
	
	public int Height()
	{
		return height;
	}
	
}
